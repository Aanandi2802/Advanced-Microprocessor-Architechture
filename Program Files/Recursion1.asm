DATA_HERE SEGMENT
	N DB 03H ;; FACTORIAL OF 3=3*2=6//SHOULD BE DISPLAYED IN O/P
	FACT DW ?
DATA_HERE ENDS

STACK_HERE SEGMENT
	DW 50 DUP(0)
	STCAK_TOP LABEL WORD
STACK_HERE ENDS

CODE_HERE SEGMENT
	ASSUME CS:CODE_HERE, DS:DATA_HERE, SS:STACK_HERE

START: 
	MOV AX, DATA_HERE
	MOV DS, AX
	MOV AX, STACK_HERE
	MOV SS, AX
	MOV SP, OFFSET STCAK_TOP
	MOV AX, 1
	MOV BL, N
	MOV BH, 0
	
	CALL FACTORIAL
	MOV FACT, AX
	INT 3H
	FACTORIAL PROC
	CMP BX, 1
	JE L1
	PUSH BX
	DEC BX
	CALL FACTORIAL
	POP BX
	MUL BX
	
L1:	RET

FACTORIAL ENDP

CODE_HERE ENDS
END START