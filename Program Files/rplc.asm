DATA SEGMENT
    MESSAGE DB "HELLO AANANDI WELCOME TO 8086 MICROPROCESSOR $"
	LEN EQU $-MESSAGE
DATA ENDS

CODE SEGMENT
	ASSUME DS: DATA, CS: CODE

START:
	MOV AX,DATA
	MOV DS,AX
	
	MOV AH,09H
	MOV BX,SEG MESSAGE 
	MOV DS,BX
	MOV DX,OFFSET MESSAGE 
	INT 21H
	
	MOV AH,02H 
	MOV DL,0AH 
	INT 21H

	MOV AL,LEN 
	LEA BX,MESSAGE

LOOP1: 
	CMP MESSAGE[BX],65H 
	JE RPLC
	CMP MESSAGE[BX],45H 
	JE RPLC
	INC BX
	DEC AL

	CMP AL,00H 
	JNE LOOP1

SHOW: 
	MOV AH,09H
	MOV BX,SEG MESSAGE
	MOV DS,BX
	MOV DX,OFFSET MESSAGE 
	INT 21H

EXIT: 
	INT 3H

RPLC: 
	ADD MESSAGE[BX],0AH 
	DEC AL
	CMP AL,00H
	JNE LOOP1 

CODE ENDS
END START